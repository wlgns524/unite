package com.rightcode.unite.network.requester.auth;

import android.content.Context;

import com.rightcode.unite.Util.DataEnums;
import com.rightcode.unite.network.requester.AbstractRequester;

import java.util.HashMap;

import lombok.Setter;
import retrofit2.Call;

public class SignInRequester extends AbstractRequester {

    //----------------------------------------------------------------------------------------------
    // static final fields
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // fields
    //----------------------------------------------------------------------------------------------

    @Setter
    private String socialPk;
    @Setter
    private String password;
    @Setter
    private DataEnums.Provider provider;
    @Setter
    private String name;
    @Setter
    private String birthDay;
    @Setter
    private String gender;

    //----------------------------------------------------------------------------------------------
    // constructor
    //----------------------------------------------------------------------------------------------

    public SignInRequester(Context context) {
        super(context);
    }

    //----------------------------------------------------------------------------------------------
    // life cycle
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // override
    //----------------------------------------------------------------------------------------------

    @Override
    protected Call genApi() {
        HashMap<String, String> map = new HashMap<>();
        map.put("socialPk", socialPk);
        map.put("password", password);
        map.put("provider", provider.name());
        map.put("name", name);
        map.put("birthDay", birthDay);
        map.put("gender", gender);

        return networkManager.getApi().signIn(map);
    }

    //----------------------------------------------------------------------------------------------
    // public
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // protected
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // private
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // inner class
    //----------------------------------------------------------------------------------------------
}
