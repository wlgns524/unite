package com.rightcode.unite.network.requester.inquiry;

import android.content.Context;

import com.rightcode.unite.network.requester.AbstractRequester;

import java.util.HashMap;

import lombok.Setter;
import retrofit2.Call;

public class InquiryRegisterRequester extends AbstractRequester {

    //----------------------------------------------------------------------------------------------
    // static final fields
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // fields
    //----------------------------------------------------------------------------------------------

    @Setter
    private String tel;
    @Setter
    private String title;
    @Setter
    private String content;
    @Setter
    private String email;


    //----------------------------------------------------------------------------------------------
    // constructor
    //----------------------------------------------------------------------------------------------

    public InquiryRegisterRequester(Context context) {
        super(context);
    }

    //----------------------------------------------------------------------------------------------
    // life cycle
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // override
    //----------------------------------------------------------------------------------------------

    @Override
    protected Call genApi() {
        HashMap<String, String> map = new HashMap<>();
        map.put("tel", tel);
        map.put("title", title);
        map.put("content", content);
        map.put("email", email);

        return networkManager.getApi().inquiryRegister(map);
    }
    //----------------------------------------------------------------------------------------------
    // public
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // protected
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // private
    //----------------------------------------------------------------------------------------------

    //----------------------------------------------------------------------------------------------
    // inner class
    //----------------------------------------------------------------------------------------------
}
